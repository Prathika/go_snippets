package main

import "fmt"

func main() {
	// simple print statement
	fmt.Println("hello-world")

	// variable assigning
	var name = "golang"
	fmt.Println(name)

	// declaring type of variable
	var year int
	year = 2023
	fmt.Println(year)

	// without declaring
	version := "v 1.21.0"
	fmt.Println(version)
}
